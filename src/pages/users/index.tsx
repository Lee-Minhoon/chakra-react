import { useCreateTestUsers, useCreateUser, useResetTestUsers } from "@/apis";
import Layout from "@/components/Layout";
import CreateUserModal from "@/containers/users/CreateUserModal";
import UsersAll from "@/containers/users/UsersAll";
import UsersByCursor from "@/containers/users/UsersByCursor";
import UsersByOffset from "@/containers/users/UsersByOffset";
import {
  Button,
  Divider,
  Flex,
  Tab,
  TabList,
  TabPanel,
  TabPanels,
  Tabs,
  useDisclosure,
} from "@chakra-ui/react";
import Head from "next/head";

const tabs = [
  "Users",
  "Users (Offset)",
  "Users (Cursor + Button)",
  "Users (Cursor + Observer)",
];
const count = 50;

const randomString = (length: number) => {
  const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
  let str = "";
  for (let i = 0; i < length; i++) {
    str += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return str;
};

const randomPhone = () => {
  const chars = "0123456789";
  let str = "010-";
  for (let i = 0; i < 4; i++) {
    str += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  str += "-";
  for (let i = 0; i < 4; i++) {
    str += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return str;
};

const Users = () => {
  const { mutate: postUser } = useCreateUser();
  const { mutate: createTestUsers, isLoading: createTestUsersLoading } =
    useCreateTestUsers(count);
  const { mutate: resetTestUsers, isLoading: restTestUsersLoading } =
    useResetTestUsers();
  const { isOpen, onOpen, onClose } = useDisclosure();

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <CreateUserModal isOpen={isOpen} onClose={onClose} />
        <Flex direction={"column"} gap={4}>
          <Flex gap={4}>
            <Button flex={1} onClick={onOpen}>
              Create User
            </Button>
            <Button
              flex={1}
              onClick={() =>
                postUser({
                  name: randomString(10),
                  email: `${randomString(20)}@gmail.com`,
                  phone: randomPhone(),
                })
              }
            >
              Create Random User
            </Button>
            <Button
              flex={1}
              onClick={() => createTestUsers({})}
              isDisabled={createTestUsersLoading}
            >
              {`Create ${count} Users`}
            </Button>
            <Button
              flex={1}
              onClick={() => resetTestUsers({})}
              isDisabled={restTestUsersLoading}
            >
              {`Reset Users`}
            </Button>
          </Flex>
          <Divider />
          <Tabs variant="enclosed">
            <TabList>
              {tabs.map((tab) => (
                <Tab key={tab}>{tab}</Tab>
              ))}
            </TabList>
            <TabPanels>
              <TabPanel>
                <UsersAll />
              </TabPanel>
              <TabPanel>
                <UsersByOffset />
              </TabPanel>
              <TabPanel>
                <UsersByCursor />
              </TabPanel>
              <TabPanel>
                <UsersByCursor observe />
              </TabPanel>
            </TabPanels>
          </Tabs>
        </Flex>
      </Layout>
    </>
  );
};

export default Users;
